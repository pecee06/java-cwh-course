package MyPackage;

public class Arithmetic{
	public int add(int a, int b){
		int val = a+b;
		return val;
	}

	public int sub(int a, int b){
		int val = a-b;
		return val;
	}

	public int mul(int a, int b){
		int val = a*b;
		return val;
	}

	public double div(double a, double b){
		double val = a/b;
		return val;
	}
}