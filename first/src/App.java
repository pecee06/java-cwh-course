// Some basics

// A java package is a group of similar types of classes, interfaces and sub-packages
// 2 types of packages --> built-in & user-defined
// Java package is used to categorize the classes and interfaces so that they can be easily maintained
// package keyword is used to create packages in java

// A class is a group of objects which have common properties. It is a template or blueprint from which objects are created
// It is a logical entity and cannot be physical
// A class can contain :
    // fields, methods, constructors and blocks
// class keyword is used to create classes in java
// For classes we use pascal naming convention
    // Eg: ThisIsJava

// A java object is a member (also called instance) of a java class
// Each object has an identity, a behaviour and a state
// The state of an object is stored in variables, while functions display the object's behaviour
// For methods we use camel-case naming convention
    // Eg: thisIsJava

// The public keyword is an acces modifier used for classes, attributes, methods and constructors, making them accessible by any other class

// The static keyword in java is used for memory management mainly
// We can apply static keyword with variables, methods, blocks and nested classes
// The static keyword belongs to the class rather than an instance of that class

// The void keyword specifies that a method should not have a return value

// main() method is the starting point for JVM to start execution of a java program
// Without main() JVM will not execute the program